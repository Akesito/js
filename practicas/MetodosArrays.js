/*
Push - Agrega al final del Array
Pop - Elimina el ultimo elemento del Array
Unshift - Elimina el primer elemento del array
Shift - Agrega al principio del array
*/

let numeros =[1, 2, 3, 4]

//Agrega al primer elemento del array
numeros.unshift('primero')

//Elimina el primer elemento del array
numeros.shift()

console.log(numeros)